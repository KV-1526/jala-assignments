public class EqualAndNotEqualOperators {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        // Equal operator (==)
        if (x == y) {
            System.out.println("x and y are equal.");
        } else {
            System.out.println("x and y are not equal.");
        }

        // Not equal operator (!=)
        if (x != y) {
            System.out.println("x and y are not equal.");
        } else {
            System.out.println("x and y are equal.");
        }
    }
}