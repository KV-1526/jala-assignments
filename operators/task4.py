num1 = 10
num2 = 5

# Less than (<) operator
if num1 < num2:
  print(f"{num1} is less than {num2}")
else:
  print(f"{num1} is not less than {num2}")

# Less than or equal to (<=) operator
if num1 <= num2:
  print(f"{num1} is less than or equal to {num2}")
else:
  print(f"{num1} is not less than or equal to {num2}")

# Greater than (>) operator
if num1 > num2:
  print(f"{num1} is greater than {num2}")
else:
  print(f"{num1} is not greater than {num2}")

# Greater than or equal to (>=) operator
if num1 >= num2:
  print(f"{num1} is greater than or equal to {num2}")
else:
  print(f"{num1} is not greater than or equal to {num2}")