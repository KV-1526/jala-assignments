# Function to demonstrate explicit type conversion
def explicit_type_conversion():
    # Input: Taking input from the user
    i = input("Enter a value for I: ")
    
    # Explicitly converting the input to an integer (from string)
    i = int(i)
    
    # Output: Displaying the value of I
    print(f"Value of I is {i}")

# Call the function
explicit_type_conversion()
